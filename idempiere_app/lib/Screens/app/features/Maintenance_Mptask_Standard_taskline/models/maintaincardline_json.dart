class MaintainCardLineJSON {
  final int? pagecount;
  final int? recordssize;
  final int? skiprecords;
  final int? rowcount;
  final int? arraycount;
  final List<Records>? records;

  MaintainCardLineJSON({
    this.pagecount,
    this.recordssize,
    this.skiprecords,
    this.rowcount,
    this.arraycount,
    this.records,
  });

  MaintainCardLineJSON.fromJson(Map<String, dynamic> json)
      : pagecount = json['page-count'] as int?,
        recordssize = json['records-size'] as int?,
        skiprecords = json['skip-records'] as int?,
        rowcount = json['row-count'] as int?,
        arraycount = json['array-count'] as int?,
        records = (json['records'] as List?)
            ?.map((dynamic e) => Records.fromJson(e as Map<String, dynamic>))
            .toList();

  Map<String, dynamic> toJson() => {
        'page-count': pagecount,
        'records-size': recordssize,
        'skip-records': skiprecords,
        'row-count': rowcount,
        'array-count': arraycount,
        'records': records?.map((e) => e.toJson()).toList()
      };
}

class Records {
  final int? id;
  final String? uid;
  final ADClientID? aDClientID;
  final ADOrgID? aDOrgID;
  final int? assay;
  final String? created;
  final CreatedBy? createdBy;
  final CUOMID? cUOMID;
  final String? description;
  final bool? isActive;
  final int? line;
  final MProductID? mProductID;
  final LITProductCardID? lITProductCardID;
  final num? qtyBOM;
  final String? updated;
  final UpdatedBy? updatedBy;
  final String? validFrom;
  final String? validTo;
  final String? serNo;
  final MProductToID? mProductToID;
  final int? qty;
  final String? name;
  final String? modelname;

  Records({
    this.id,
    this.uid,
    this.aDClientID,
    this.aDOrgID,
    this.assay,
    this.created,
    this.createdBy,
    this.cUOMID,
    this.description,
    this.isActive,
    this.line,
    this.mProductID,
    this.lITProductCardID,
    this.qtyBOM,
    this.updated,
    this.updatedBy,
    this.validFrom,
    this.validTo,
    this.serNo,
    this.mProductToID,
    this.qty,
    this.name,
    this.modelname,
  });

  Records.fromJson(Map<String, dynamic> json)
      : id = json['id'] as int?,
        uid = json['uid'] as String?,
        aDClientID = (json['AD_Client_ID'] as Map<String, dynamic>?) != null
            ? ADClientID.fromJson(json['AD_Client_ID'] as Map<String, dynamic>)
            : null,
        aDOrgID = (json['AD_Org_ID'] as Map<String, dynamic>?) != null
            ? ADOrgID.fromJson(json['AD_Org_ID'] as Map<String, dynamic>)
            : null,
        assay = json['Assay'] as int?,
        created = json['Created'] as String?,
        createdBy = (json['CreatedBy'] as Map<String, dynamic>?) != null
            ? CreatedBy.fromJson(json['CreatedBy'] as Map<String, dynamic>)
            : null,
        cUOMID = (json['C_UOM_ID'] as Map<String, dynamic>?) != null
            ? CUOMID.fromJson(json['C_UOM_ID'] as Map<String, dynamic>)
            : null,
        description = json['Description'] as String?,
        isActive = json['IsActive'] as bool?,
        line = json['Line'] as int?,
        mProductID = (json['M_Product_ID'] as Map<String, dynamic>?) != null
            ? MProductID.fromJson(json['M_Product_ID'] as Map<String, dynamic>)
            : null,
        lITProductCardID =
            (json['LIT_ProductCard_ID'] as Map<String, dynamic>?) != null
                ? LITProductCardID.fromJson(
                    json['LIT_ProductCard_ID'] as Map<String, dynamic>)
                : null,
        qtyBOM = json['QtyBOM'] as num?,
        updated = json['Updated'] as String?,
        updatedBy = (json['UpdatedBy'] as Map<String, dynamic>?) != null
            ? UpdatedBy.fromJson(json['UpdatedBy'] as Map<String, dynamic>)
            : null,
        validFrom = json['ValidFrom'] as String?,
        validTo = json['ValidTo'] as String?,
        serNo = json['SerNo'] as String?,
        mProductToID =
            (json['M_Product_To_ID'] as Map<String, dynamic>?) != null
                ? MProductToID.fromJson(
                    json['M_Product_To_ID'] as Map<String, dynamic>)
                : null,
        qty = json['Qty'] as int?,
        name = json['Name'] as String?,
        modelname = json['model-name'] as String?;

  Map<String, dynamic> toJson() => {
        'id': id,
        'uid': uid,
        'AD_Client_ID': aDClientID?.toJson(),
        'AD_Org_ID': aDOrgID?.toJson(),
        'Assay': assay,
        'Created': created,
        'CreatedBy': createdBy?.toJson(),
        'C_UOM_ID': cUOMID?.toJson(),
        'Description': description,
        'IsActive': isActive,
        'Line': line,
        'M_Product_ID': mProductID?.toJson(),
        'LIT_ProductCard_ID': lITProductCardID?.toJson(),
        'QtyBOM': qtyBOM,
        'Updated': updated,
        'UpdatedBy': updatedBy?.toJson(),
        'ValidFrom': validFrom,
        'ValidTo': validTo,
        'SerNo': serNo,
        'M_Product_To_ID': mProductToID?.toJson(),
        'Qty': qty,
        'Name': name,
        'model-name': modelname
      };
}

