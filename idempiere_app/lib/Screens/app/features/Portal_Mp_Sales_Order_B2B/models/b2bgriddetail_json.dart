class B2BGridDetailJSON {
  final int? pagecount;
  final int? recordssize;
  final int? skiprecords;
  final int? rowcount;
  final int? arraycount;
  final List<Records>? records;

  B2BGridDetailJSON({
    this.pagecount,
    this.recordssize,
    this.skiprecords,
    this.rowcount,
    this.arraycount,
    this.records,
  });

  B2BGridDetailJSON.fromJson(Map<String, dynamic> json)
      : pagecount = json['page-count'] as int?,
        recordssize = json['records-size'] as int?,
        skiprecords = json['skip-records'] as int?,
        rowcount = json['row-count'] as int?,
        arraycount = json['array-count'] as int?,
        records = (json['records'] as List?)
            ?.map((dynamic e) => Records.fromJson(e as Map<String, dynamic>))
            .toList();

  Map<String, dynamic> toJson() => {
        'page-count': pagecount,
        'records-size': recordssize,
        'skip-records': skiprecords,
        'row-count': rowcount,
        'array-count': arraycount,
        'records': records?.map((e) => e.toJson()).toList()
      };
}

class Records {
  final int? id;
  final String? sKU;
  final String? stringAgg;
  final String? prices;
  final String? qtyAvailable;
  final String? qtyOrdered;
  final String? qtyOnHand;
  final String? dateRestocks;
  final bool? isActive;
  final String? created;
  final CreatedBy? createdBy;
  final String? updated;
  final UpdatedBy? updatedBy;
  final String? modelname;

  Records({
    this.id,
    this.sKU,
    this.stringAgg,
    this.prices,
    this.qtyAvailable,
    this.qtyOrdered,
    this.qtyOnHand,
    this.dateRestocks,
    this.isActive,
    this.created,
    this.createdBy,
    this.updated,
    this.updatedBy,
    this.modelname,
  });

  Records.fromJson(Map<String, dynamic> json)
      : id = json['id'] as int?,
        sKU = json['SKU'] as String?,
        stringAgg = json['string_agg'] as String?,
        prices = json['prices'] as String?,
        qtyAvailable = json['QtyAvailable'] as String?,
        qtyOrdered = json['QtyOrdered'] as String?,
        qtyOnHand = json['QtyOnHand'] as String?,
        dateRestocks = json['daterestocks'] as String?,
        isActive = json['IsActive'] as bool?,
        created = json['Created'] as String?,
        createdBy = (json['CreatedBy'] as Map<String, dynamic>?) != null
            ? CreatedBy.fromJson(json['CreatedBy'] as Map<String, dynamic>)
            : null,
        updated = json['Updated'] as String?,
        updatedBy = (json['UpdatedBy'] as Map<String, dynamic>?) != null
            ? UpdatedBy.fromJson(json['UpdatedBy'] as Map<String, dynamic>)
            : null,
        modelname = json['model-name'] as String?;

  Map<String, dynamic> toJson() => {
        'id': id,
        'SKU': sKU,
        'string_agg': stringAgg,
        'prices': prices,
        'QtyAvailable': qtyAvailable,
        'QtyOrdered': qtyOrdered,
        'daterestocks': dateRestocks,
        'QtyOnHand': qtyOnHand,
        'IsActive': isActive,
        'Created': created,
        'CreatedBy': createdBy?.toJson(),
        'Updated': updated,
        'UpdatedBy': updatedBy?.toJson(),
        'model-name': modelname
      };
}

