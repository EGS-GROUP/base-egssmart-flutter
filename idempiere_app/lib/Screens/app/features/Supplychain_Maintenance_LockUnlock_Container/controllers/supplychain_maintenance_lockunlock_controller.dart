  getMaintainResourcesByContainer(String character) async {
    final ip = GetStorage().read('ip');
    String authorization = 'Bearer ${GetStorage().read('token')}';
    final protocol = GetStorage().read('protocol');
    var url = Uri.parse(
        '$protocol://$ip/api/v1/models/mp_maintain_resource?\$filter= Lot eq \'${containerFieldController.text}$character\'');

    var response = await http.get(
      url,
      headers: <String, String>{
        'Content-Type': 'application/json',
        'Authorization': authorization,
      },
    );

    if (response.statusCode == 200) {
      var json = WorkOrderResourceLocalJson.fromJson(jsonDecode(response.body));

      var replacedString = "";

      if ((containerFieldController.text + character).contains('U')) {
        replacedString =
            (containerFieldController.text + character).replaceAll('U', 'L');
      }

      if ((containerFieldController.text + character).contains('L')) {
        replacedString =
            (containerFieldController.text + character).replaceAll('L', 'U');
      }

      Get.defaultDialog(
          title: "Lock/Unlock Container".tr,
          content: Text(
            "${json.records!.length} record, ${"proceed".tr}?",
          ),
          textCancel: "No".tr,
          textCustom: "Yes".tr,
          onConfirm: () {
            for (var element in json.records!) {
              editContainerMaintainResource(element.id!, replacedString);
            }
            Get.back();

            Get.snackbar(
              "${json.records!.length} Done!".tr,
              "Container Status Changed".tr,
              icon: const Icon(
                Icons.done,
                color: Colors.green,
              ),
            );
          });
    }
  }

  editContainerMaintainResource(int id, String text) async {
    var msg = {"Lot": text};

    final ip = GetStorage().read('ip');
    String authorization = 'Bearer ${GetStorage().read('token')}';
    final protocol = GetStorage().read('protocol');
    var url =
        Uri.parse('$protocol://$ip/api/v1/models/mp_maintain_resource/$id');

    var response = await http.put(
      url,
      body: jsonEncode(msg),
      headers: <String, String>{
        'Content-Type': 'application/json',
        'Authorization': authorization,
      },
    );

    if (response.statusCode == 200) {
      dataAvailable.value = false;

      //print(response.body);
    } else {
      if (kDebugMode) {
        print(response.body);
      }
    }
  }

