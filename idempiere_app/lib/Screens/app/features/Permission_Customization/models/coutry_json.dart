class CountryJSON {
  final int? pagecount;
  final int? recordssize;
  final int? skiprecords;
  final int? rowcount;
  final int? arraycount;
  final List<CtrRecords>? records;

  CountryJSON({
    this.pagecount,
    this.recordssize,
    this.skiprecords,
    this.rowcount,
    this.arraycount,
    this.records,
  });

  CountryJSON.fromJson(Map<String, dynamic> json)
      : pagecount = json['page-count'] as int?,
        recordssize = json['records-size'] as int?,
        skiprecords = json['skip-records'] as int?,
        rowcount = json['row-count'] as int?,
        arraycount = json['array-count'] as int?,
        records = (json['records'] as List?)
            ?.map((dynamic e) => CtrRecords.fromJson(e as Map<String, dynamic>))
            .toList();

  Map<String, dynamic> toJson() => {
        'page-count': pagecount,
        'records-size': recordssize,
        'skip-records': skiprecords,
        'row-count': rowcount,
        'array-count': arraycount,
        'records': records?.map((e) => e.toJson()).toList()
      };
}

class CtrRecords {
  final int? id;
  final String? uid;
  final ADClientID? aDClientID;
  final ADOrgID? aDOrgID;
  final bool? isActive;
  final String? created;
  final CreatedBy? createdBy;
  final String? updated;
  final UpdatedBy? updatedBy;
  final String? name;
  final String? description;
  final String? countryCode;
  final bool? hasRegion;
  final String? displaySequence;
  final bool? hasPostalAdd;
  final CCurrencyID? cCurrencyID;
  final bool? isAddressLinesLocalReverse;
  final bool? isAddressLinesReverse;
  final bool? isPostcodeLookup;
  final bool? allowCitiesOutOfList;
  final String? captureSequence;
  final String? modelname;

  CtrRecords({
    this.id,
    this.uid,
    this.aDClientID,
    this.aDOrgID,
    this.isActive,
    this.created,
    this.createdBy,
    this.updated,
    this.updatedBy,
    this.name,
    this.description,
    this.countryCode,
    this.hasRegion,
    this.displaySequence,
    this.hasPostalAdd,
    this.cCurrencyID,
    this.isAddressLinesLocalReverse,
    this.isAddressLinesReverse,
    this.isPostcodeLookup,
    this.allowCitiesOutOfList,
    this.captureSequence,
    this.modelname,
  });

  CtrRecords.fromJson(Map<String, dynamic> json)
      : id = json['id'] as int?,
        uid = json['uid'] as String?,
        aDClientID = (json['AD_Client_ID'] as Map<String, dynamic>?) != null
            ? ADClientID.fromJson(json['AD_Client_ID'] as Map<String, dynamic>)
            : null,
        aDOrgID = (json['AD_Org_ID'] as Map<String, dynamic>?) != null
            ? ADOrgID.fromJson(json['AD_Org_ID'] as Map<String, dynamic>)
            : null,
        isActive = json['IsActive'] as bool?,
        created = json['Created'] as String?,
        createdBy = (json['CreatedBy'] as Map<String, dynamic>?) != null
            ? CreatedBy.fromJson(json['CreatedBy'] as Map<String, dynamic>)
            : null,
        updated = json['Updated'] as String?,
        updatedBy = (json['UpdatedBy'] as Map<String, dynamic>?) != null
            ? UpdatedBy.fromJson(json['UpdatedBy'] as Map<String, dynamic>)
            : null,
        name = json['Name'] as String?,
        description = json['Description'] as String?,
        countryCode = json['CountryCode'] as String?,
        hasRegion = json['HasRegion'] as bool?,
        displaySequence = json['DisplaySequence'] as String?,
        hasPostalAdd = json['HasPostal_Add'] as bool?,
        cCurrencyID = (json['C_Currency_ID'] as Map<String, dynamic>?) != null
            ? CCurrencyID.fromJson(
                json['C_Currency_ID'] as Map<String, dynamic>)
            : null,
        isAddressLinesLocalReverse =
            json['IsAddressLinesLocalReverse'] as bool?,
        isAddressLinesReverse = json['IsAddressLinesReverse'] as bool?,
        isPostcodeLookup = json['IsPostcodeLookup'] as bool?,
        allowCitiesOutOfList = json['AllowCitiesOutOfList'] as bool?,
        captureSequence = json['CaptureSequence'] as String?,
        modelname = json['model-name'] as String?;

  Map<String, dynamic> toJson() => {
        'id': id,
        'uid': uid,
        'AD_Client_ID': aDClientID?.toJson(),
        'AD_Org_ID': aDOrgID?.toJson(),
        'IsActive': isActive,
        'Created': created,
        'CreatedBy': createdBy?.toJson(),
        'Updated': updated,
        'UpdatedBy': updatedBy?.toJson(),
        'Name': name,
        'Description': description,
        'CountryCode': countryCode,
        'HasRegion': hasRegion,
        'DisplaySequence': displaySequence,
        'HasPostal_Add': hasPostalAdd,
        'C_Currency_ID': cCurrencyID?.toJson(),
        'IsAddressLinesLocalReverse': isAddressLinesLocalReverse,
        'IsAddressLinesReverse': isAddressLinesReverse,
        'IsPostcodeLookup': isPostcodeLookup,
        'AllowCitiesOutOfList': allowCitiesOutOfList,
        'CaptureSequence': captureSequence,
        'model-name': modelname
      };
}

